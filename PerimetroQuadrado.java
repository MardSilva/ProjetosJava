public class PerimetroQuadrado{ // delcarando a classe PerimetroQuadrado

   public static void main (String [] args){ //criando o método main para classe perímetro quadrado
   
   // declarando a variável lado com valor igual a 4
   double lado = 7.0;
   
   // declarando a variável perímetro
   double perimetro = 4 * lado;
   
   
   // escrevendo o resultado na tela
   System.out.println("\nO valor do lado de um quadrado e: " + lado);
   System.out.println("\nO perimetro do quadrado e > " + perimetro);
   
   }
}